package calculator;

public class Calculator {

    public double add(int a, int b) {
        return a + b;
    }

    public double substract(int a, int b) {
        return  a - b;
    }
    public double multiply(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b) {
        return  a / b;
    }
}
